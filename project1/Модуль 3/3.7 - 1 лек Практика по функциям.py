# Список с уникальными элементами
def unique_list(list_3):
    # Создадим переменную, которая будет хранить список
    new_list = []
    for i in list_3:
        # Если элемента i нет в списке new_list, то добавляю его в конец списка
        if i not in new_list:
            new_list.append(i)
    return print('Список с уникальными элементами:', new_list)


# Вызываю функцию unique_list со списком [1, 2, 3, 45, 1, 453, 578, 23, 5, 2, 3]
unique_list([1, 2, 3, 45, 1, 453, 578, 23, 5, 2, 3])
