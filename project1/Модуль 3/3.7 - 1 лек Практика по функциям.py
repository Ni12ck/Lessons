# Максимум в списке
def find_max(list_1):
    # Создадим переменную max_, для нахождения максимального значения
    max_ = list_1[0]
    # Циклом for пройдём по списку list_1
    for i in list_1:
        # Если i больше max_, то перезаписываем его значение
        if i > max_:
            max_ = i
    return print('Максимальное значение в списке:', max_)


# Вызываю функцию find_max со списком [157, 2, 3, 6, 12, 32]
find_max([157, 2, 3, 6, 12, 32])


